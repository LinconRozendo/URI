#include<stdio.h>

x = int(input())


for x in range(0, x):
    n = int(input())
    if n == 0:
        print('NULL')
    else:
        if n > 0:
            if (n % 2) == 0:
                print('EVEN POSITIVE')
            else:
                print('ODD POSITIVE')
        else:
            if (n % 2) == 0:
                print('EVEN NEGATIVE')
            else:
                print('ODD NEGATIVE')